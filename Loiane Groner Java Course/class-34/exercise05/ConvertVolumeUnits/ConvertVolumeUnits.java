package exercise05.ConvertVolumeUnits;

public class ConvertVolumeUnits {
  public static int litersToCm3 (int liters) {
    return liters * 1000;
  }

  public static double litersToCm3 (double liters) {
    return liters * 1000;
  }

  public static int m3ToLiters (int m3) {
    return m3 * 1000;
  }

  public static double m3ToLiters (double m3) {
    return m3 * 1000;
  }

  public static double m3ToFt3 (int m3) {
    return m3 * 35.32;
  }

  public static double m3ToFt3 (double m3) {
    return m3 * 35.32;
  }

  public static int gallonToIn3 (int gallon) {
    return gallon * 231;
  }

  public static double gallonToIn3 (double gallon) {
    return gallon * 231;
  }

  public static double gallonToLiter (int gallon) {
    return gallon * 3.785;
  }

  public static double gallonToLiter (double gallon) {
    return gallon * 3.785;
  }
}
