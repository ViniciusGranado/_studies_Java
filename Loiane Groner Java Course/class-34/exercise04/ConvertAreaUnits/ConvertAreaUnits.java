package exercise04.ConvertAreaUnits;

public class ConvertAreaUnits {
  public static double meterToFeet (int meters) {
    return meters * 10.76;
  }

  public static double meterToFeet (double meters) {
    return meters * 10.76;
  }

  public static int feetToCm (int feet) {
    return feet * 929;
  }

  public static double feetToCm (double feet) {
    return feet * 929;
  }

  public static int mileToAcre (int mile) {
    return mile * 640;
  }

  public static double mileToAcre (double mile) {
    return mile * 640;
  }

  public static double acreToFeet (int acre) {
    return acre * 43560;
  }

  public static double acreToFeet (double acre) {
    return acre * 43560;
  }
}
